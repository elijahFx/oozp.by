export interface ApiResponse {
  success: boolean;
  data: Article[];
  error?: string;
}

const API_BASE_URL = 'https://apocrypha.su';

export async function fetchArticles(): Promise<Article[]> {
  try {
    const response = await fetch(`${API_BASE_URL}/articles`, {
      method: 'GET',
      headers: {
        'Content-Type': 'application/json',
        'Accept': 'application/json',
      },
      // Add cache control for better performance
      next: { revalidate: 300 }, // Revalidate every 5 minutes
    });

    if (!response.ok) {
      throw new Error(`HTTP error! status: ${response.status}`);
    }

    const result: ApiResponse = await response.json();

    // Transform API response to match our interface
    const articles = Array.isArray(result) ? result : (result.data || []);
    return articles.map(transformArticle);
  } catch (error) {
    console.error('Error fetching articles:', error);
    
    // Return fallback articles if API fails
    return getFallbackArticles();
  }
}

export async function fetchArticleById(id: string): Promise<Article | null> {
  try {
    const response = await fetch(`${API_BASE_URL}/articles/${id}`, {
      method: 'GET',
      headers: {
        'Content-Type': 'application/json',
        'Accept': 'application/json',
      },
      next: { revalidate: 300 },
    });

    if (!response.ok) {
      if (response.status === 404) {
        return null;
      }
      throw new Error(`HTTP error! status: ${response.status}`);
    }

    const article = await response.json();
    return article ? transformArticle(article) : null;
  } catch (error) {
    console.error(`Error fetching article ${id}:`, error);
    
    // Return fallback article if API fails
    return getFallbackArticleById(id);
  }
}

// Transform API article to match our interface
